import Head from 'next/head';
import { useCallback, useState } from 'react';
import { useSession } from 'next-auth/react';
import {
  ActionIcon,
  Anchor,
  Avatar,
  Container,
  Divider,
  Group,
  rem,
  SegmentedControl,
  Stack,
  Text,
  Title,
  Tooltip,
} from '@mantine/core';
import { notifications } from '@mantine/notifications';
import { IconDeviceFloppy } from '@tabler/icons-react';
import { GetServerSideProps, InferGetServerSidePropsType } from 'next';
import { Header } from '@/components/Header';
import { getClient } from '@/lib';
import { Track } from '@/lib/spotify';
import {
  CreatePlaylistResponse,
  type CreatePlaylistRequest,
} from '../api/create-playlist';
import dayjs from 'dayjs';
import { upperFirst } from '@mantine/hooks';
import Link from 'next/link';

interface User {
  name: string;
  id: string;
  image: string;
}
type TimeRange = 'short' | 'medium' | 'long';
interface UserPageProps {
  topTracks: Record<TimeRange, Track[]>;
  user: User;
}

export const getServerSideProps: GetServerSideProps<UserPageProps> = async (
  context
) => {
  const id = context.params?.id;

  if (typeof id !== 'string') {
    throw new Error(`Error id not found`);
  }

  const [, client] = await getClient(id);
  const profile = await client.getUserProfile(id);
  const profileImage = profile.images.sort(
    (a, b) => b.width ?? 0 - (a.width ?? 0)
  )[0].url;

  const timeRanges = ['short', 'medium', 'long'] as const;
  const [short, medium, long] = await Promise.all(
    timeRanges.map((timeRange) =>
      client.getTopTracks({ limit: 5, offset: 0, timeRange }).then((r) => {
        if ('error' in r) {
          throw new Error(`Error with ${timeRange}`);
        }
        return r.items;
      })
    )
  );

  return {
    props: {
      topTracks: {
        short,
        medium,
        long,
      },
      user: {
        id,
        name: profile.display_name,
        image: profileImage,
      },
    },
  };
};

const PlaylistCreator = ({
  user,
  tracks,
  timeRange,
}: {
  user: Omit<User, 'image'>;
  tracks: Track[];
  timeRange: TimeRange;
}) => {
  const session = useSession();

  const create = useCallback(() => {
    if (tracks.length === 0) {
      return;
    }
    const date = dayjs().format('YYYY-MM-DD');

    const request: CreatePlaylistRequest = {
      trackUris: tracks.map((track) => track.uri),
      name: `top-tracks-${user.name}-${timeRange}-${date}`,
      desc: `${user.name}'s Top Tracks of ${upperFirst(
        timeRange
      )} Time Range at ${date}`,
    };
    fetch('/api/create-playlist', {
      method: 'POST',
      body: JSON.stringify(request),
      headers: { 'content-type': 'application/json' },
    }).then(async (resp) => {
      if (resp.ok) {
        const data: CreatePlaylistResponse = await resp.json();
        notifications.show({
          title: 'プレイリストを作成しました!',
          message: (
            <>
              <Anchor component={Link} target="_blank" href={data.playlist.url}>
                {data.playlist.name}
              </Anchor>
              <Text>という名前で追加しました</Text>
            </>
          ),
        });
      }
    });
  }, [tracks]);

  return (
    <Tooltip label="プレイリストを作成する">
      <ActionIcon
        onClick={create}
        disabled={session === null}
        variant="filled"
        color="teal"
      >
        <IconDeviceFloppy />
      </ActionIcon>
    </Tooltip>
  );
};

export default function UserPage({
  user,
  topTracks,
}: InferGetServerSidePropsType<typeof getServerSideProps>) {
  const [timeRange, setTimeRange] = useState<TimeRange>('short');

  return (
    <>
      <Head>
        <title>Kokage</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <Header />
        <Container my="sm">
          <Stack>
            <Group position="apart" mx={rem(10)}>
              <Group>
                <Avatar src={user.image} />
                <Title fz="md">{user.name}</Title>
              </Group>
              <SegmentedControl
                data={[
                  { label: 'Short', value: 'short' },
                  { label: 'Medium', value: 'medium' },
                  { label: 'Long', value: 'long' },
                ]}
                value={timeRange}
                onChange={(value) => setTimeRange(value as typeof timeRange)}
              />
              <PlaylistCreator
                tracks={topTracks[timeRange]}
                timeRange={timeRange}
                user={user}
              />
            </Group>
            <Divider />
            {topTracks[timeRange].map((track) => (
              <iframe
                key={track.id}
                src={`https://open.spotify.com/embed/track/${track.id}`}
                height="80"
                frameBorder="0"
                allowFullScreen
                allow="autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture"
                loading="lazy"
              ></iframe>
            ))}
          </Stack>
        </Container>
      </main>
    </>
  );
}
